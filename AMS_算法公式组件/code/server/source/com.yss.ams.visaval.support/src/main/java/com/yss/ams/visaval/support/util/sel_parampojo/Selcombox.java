package com.yss.ams.visaval.support.util.sel_parampojo;

import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlTransient;

public class Selcombox {

	@XmlAttribute(name = "code")
	private String code;

	@XmlAttribute(name = "DisplayName")
	private String DisplayName;

	@XmlAttribute(name = "DisplayValue")
	private String DisplayValue;

	@XmlAttribute(name = "Parameter")
	private String Parameter;

	@XmlAttribute(name = "YssCaption")
	private String YssCaption;

	@XmlAttribute(name = "YssIsMust")
	private String YssIsMust;

	@XmlAttribute(name = "YssLength")
	private String YssLength;

	@XmlAttribute(name = "Value")
	private String Value;

	@XmlAttribute(name = "fun")
	private String fun;

	@XmlAttribute(name = "MethodName")
	private String MethodName;

	@XmlAttribute(name = "MethodParamValues")
	private String MethodParamValues;

	@XmlTransient
	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	@XmlTransient
	public String getDisplayName() {
		return DisplayName;
	}

	public void setDisplayName(String displayName) {
		DisplayName = displayName;
	}

	@XmlTransient
	public String getDisplayValue() {
		return DisplayValue;
	}

	public void setDisplayValue(String displayValue) {
		DisplayValue = displayValue;
	}

	@XmlTransient
	public String getParameter() {
		return Parameter;
	}

	public void setParameter(String parameter) {
		Parameter = parameter;
	}

	@XmlTransient
	public String getYssCaption() {
		return YssCaption;
	}

	public void setYssCaption(String yssCaption) {
		YssCaption = yssCaption;
	}

	@XmlTransient
	public String getYssIsMust() {
		return YssIsMust;
	}

	public void setYssIsMust(String yssIsMust) {
		YssIsMust = yssIsMust;
	}

	@XmlTransient
	public String getYssLength() {
		return YssLength;
	}

	public void setYssLength(String yssLength) {
		YssLength = yssLength;
	}

	@XmlTransient
	public String getValue() {
		return Value;
	}

	public void setValue(String value) {
		Value = value;
	}

	@XmlTransient
	public String getFun() {
		return fun;
	}

	public void setFun(String fun) {
		this.fun = fun;
	}

	@XmlTransient
	public String getMethodName() {
		return MethodName;
	}

	public void setMethodName(String methodName) {
		MethodName = methodName;
	}

	@XmlTransient
	public String getMethodParamValues() {
		return MethodParamValues;
	}

	public void setMethodParamValues(String methodParamValues) {
		MethodParamValues = methodParamValues;
	}

}
