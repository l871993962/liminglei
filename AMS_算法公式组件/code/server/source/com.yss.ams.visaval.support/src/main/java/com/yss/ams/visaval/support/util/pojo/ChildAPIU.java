package com.yss.ams.visaval.support.util.pojo;

import java.util.List;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 * 对应child节点 此对象仅供解析XML封装数据临时使用
 * 
 * @author 马向峰
 * 
 */
public class ChildAPIU {

	@XmlElement(name = "function")
	private List<ChildFunU> childFunList;

	@XmlTransient
	public List<ChildFunU> getChildFunList() {
		return childFunList;
	}

	public void setChildFunList(List<ChildFunU> childFunList) {
		this.childFunList = childFunList;
	}

}
