package com.yss.ams.visaval.support.util.pojo;

import java.util.List;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 * 对应variables节点
 * 此对象仅供解析XML封装数据临时使用
 * @author 马向峰
 *
 */
public class VariableAPIU {

	@XmlElement(name = "variable")
	private List<VariableU> variableList;

	@XmlTransient
	public List<VariableU> getVariableList() {
		return variableList;
	}

	public void setVariableList(List<VariableU> variableList) {
		this.variableList = variableList;
	}

}
