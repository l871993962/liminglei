using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using FAST.Common.Service.Pojo.Base;
using FAST.Common.Service.Datastructure;
using Newtonsoft.Json;

namespace YssVisAval.Pojo.SelfAlgorithm
{
    /// <summary>
    /// 算法界面优化 新建pojo
    /// 20170710 马向峰
    /// </summary>
    [NodeDesc(ParentNode = "AlgoParent", TreeNode = "AlgoCode")]
   public class AlgoAPI : BasePojo
    {
        /// <summary>
        /// 算法code
        /// </summary>
        private string algoCode;

        /// <summary>
        /// 算法的父节点
        /// </summary>
        private string algoParent;

        /// <summary>
        /// 算法名称
        /// </summary>
        private string algoText;

        /// <summary>
        /// 算法的值
        /// </summary>
        private string algoValue;

        /// <summary>
        /// 算法的描述
        /// </summary>
        private string algoDesc;

        /// <summary>
        /// 算法代码
        /// </summary>
        [JsonProperty(PropertyName = "algoCode")]
        public string AlgoCode
        {
            get { return algoCode; }
            set { algoCode = value; }
        }

        /// <summary>
        /// 算法父节点
        /// </summary>
        [JsonProperty(PropertyName = "algoParent")]
        public string AlgoParent
        {
            get { return algoParent; }
            set { algoParent = value; }
        }

        /// <summary>
        /// 算法名称
        /// </summary>
        [JsonProperty(PropertyName = "algoText")]
        public string AlgoText
        {
            get { return algoText; }
            set { algoText = value; }
        }

        /// <summary>
        /// 算法值
        /// </summary>
        [JsonProperty(PropertyName = "algoValue")]
        public string AlgoValue
        {
            get { return algoValue; }
            set { algoValue = value; }
        }

        /// <summary>
        /// 算法描述
        /// </summary>
        [JsonProperty(PropertyName = "algoDesc")]
        public string AlgoDesc
        {
            get { return algoDesc; }
            set { algoDesc = value; }
        }

    }
}
