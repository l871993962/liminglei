using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace YssVisAval.Pojo.SelfAlgorithm
{
    /// <summary>
    /// 算法设置界面参数pojo，负责将set界面参数带到list界面
    /// </summary>
    public class AlgoSetParam
    {
        /// <summary>
        /// 算法代码
        /// </summary>
        private string algoCode;

        /// <summary>
        /// 参考算法代码
        /// </summary>
        private string c_AlgoCode;

        /// <summary>
        /// 参考算法代码
        /// </summary>
        public string C_AlgoCode
        {
            get { return c_AlgoCode; }
            set { c_AlgoCode = value; }
        }

        /// <summary>
        /// 算法代码
        /// </summary>
        public string AlgoCode
        {
            get { return algoCode; }
            set { algoCode = value; }
        }
    }
}
