package com.yss.uco.elecreco.er.task.dao;
public enum ErTaskColumnName {

	/**
	*自动ID
	*/
	id("C_IDEN"),

	/**
	*任务代码

	*/
	c_DV_TASK_CODE("C_DV_TASK_CODE"),

	/**
	*运行间隔(分钟)
	*/
	n_RUN_INTERVAL("N_RUN_INTERVAL"),

	/**
	*提醒用户
	*/
	c_CALL_USER("C_CALL_USER"),

	/**
	*当前执行状态(1-正常，0-异常)
	*/
	n_RUN_STATE("N_RUN_STATE"),

	/**
	*当前执行详细信息

	*/
	c_RUN_INFO("C_RUN_INFO"),

	/**
	*当前执行结果（0-失败，1-成功）
	*/
	n_RUN_RESULT("N_RUN_RESULT"),

	/**
	*执行结果详细信息
	*/
	c_RESULT_INFO("C_RESULT_INFO"),

	/**
	*修改人
	*/
	modifyDate("C_UPDATE_TIME"),

	/**
	*修改时间

	*/
	modifier("C_UPDATE_BY"),

	endUseDate(""),

	startUseDate("");

	private String value;

	private ErTaskColumnName(String value) {
		this.value = value;
	}
	public String toString() {
		return this.value.toString();
	}
}