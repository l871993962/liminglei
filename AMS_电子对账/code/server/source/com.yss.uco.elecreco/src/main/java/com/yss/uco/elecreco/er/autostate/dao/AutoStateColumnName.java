package com.yss.uco.elecreco.er.autostate.dao;

public enum AutoStateColumnName {
	
	id("C_IDEN"),

	c_process_id("c_process_id"),
	
	c_task_id("c_task_id"),
	
	c_port_code("c_port_code"), 
	
	c_file_type("c_file_type"),
	
	c_sn("c_sn"),
	
	c_state("c_state");
	
	private String value;

	private AutoStateColumnName(String value) {
		this.value = value;
	}

	public String toString() {
		return this.value.toString();
	}

}
