package com.yss.uco.elecreco.er.ws.pojo.dzdzstate;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 * 电子对账对外服务接口报文格式
 * @author XIAOQIRUI
 *
 */
@XmlRootElement(name="REQUEST")
@XmlAccessorType(XmlAccessType.FIELD)
public class RequestDzStateXml {

	/**
	* 版本号
	* */
	@XmlElement(name = "DS_VER")
	private String DS_VER;
	
	/**
	* 签名
	* */
	@XmlElement(name = "DS_SIGN")
	private String DS_SIGN;
	
	/**
	* 签名方式
	* */
	@XmlElement(name = "DS_SIGNTYPE")
	private String DS_SIGNTYPE;
	
	/**
	* 消息类型
	* */
	@XmlElement(name = "DS_MSG_TYPE")
	private String DS_MSG_TYPE;
	
	/**
	* 系统
	* */
	@XmlElement(name = "DS_SYS_CODE")
	private String DS_SYS_CODE;
	
	/**
	* 功能码
	* */
	@XmlElement(name = "DS_FUNC")
	private String DS_FUNC;
	
	/**
	* 通知时间
	* */
	@XmlElement(name = "DS_MSG_TM")
	private String DS_MSG_TM;
	
	/**
	* 请求ID
	* */
	@XmlElement(name = "DS_JD")
	private String DS_JD;
	
	/**
	* 消息报文体
	* */
	@XmlElement(name = "DS_MSG_BODY")
	private String DS_MSG_BODY;
	
	
	@XmlElement(name = "PROT_DZDZ")
	private DzdzCommon dzdzCommon;

	@XmlTransient
	public String getDS_VER() {
		return DS_VER;
	}

	public void setDS_VER(String dS_VER) {
		DS_VER = dS_VER;
	}
	
	@XmlTransient
	public String getDS_SIGN() {
		return DS_SIGN;
	}

	public void setDS_SIGN(String dS_SIGN) {
		DS_SIGN = dS_SIGN;
	}
	
	@XmlTransient
	public String getDS_SIGNTYPE() {
		return DS_SIGNTYPE;
	}

	public void setDS_SIGNTYPE(String dS_SIGNTYPE) {
		DS_SIGNTYPE = dS_SIGNTYPE;
	}
	
	@XmlTransient
	public String getDS_MSG_TYPE() {
		return DS_MSG_TYPE;
	}
	
	public void setDS_MSG_TYPE(String dS_MSG_TYPE) {
		DS_MSG_TYPE = dS_MSG_TYPE;
	}
	
	@XmlTransient
	public String getDS_SYS_CODE() {
		return DS_SYS_CODE;
	}

	public void setDS_SYS_CODE(String dS_SYS_CODE) {
		DS_SYS_CODE = dS_SYS_CODE;
	}

	@XmlTransient
	public String getDS_FUNC() {
		return DS_FUNC;
	}

	public void setDS_FUNC(String dS_FUNC) {
		DS_FUNC = dS_FUNC;
	}

	@XmlTransient
	public String getDS_MSG_TM() {
		return DS_MSG_TM;
	}

	public void setDS_MSG_TM(String dS_MSG_TM) {
		DS_MSG_TM = dS_MSG_TM;
	}
	
	@XmlTransient
	public String getDS_JD() {
		return DS_JD;
	}

	public void setDS_JD(String dS_JD) {
		DS_JD = dS_JD;
	}

	@XmlTransient
	public String getDS_MSG_BODY() {
		return DS_MSG_BODY;
	}

	public void setDS_MSG_BODY(String dS_MSG_BODY) {
		DS_MSG_BODY = dS_MSG_BODY;
	}

	@XmlTransient
	public DzdzCommon getDzdzCommon() {
		return dzdzCommon;
	}

	public void setDzdzCommon(DzdzCommon dzdzCommon) {
		this.dzdzCommon = dzdzCommon;
	}
	
}
