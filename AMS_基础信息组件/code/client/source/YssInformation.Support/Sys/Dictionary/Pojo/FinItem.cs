{
    /// <summary>
    /// 财务项字典pojo
    /// </summary>
    public class FinItem : BasePojo
    {
        /// <summary>
        /// 财务项目代码
        /// </summary>
        private string c_FIN_CODE;

        /// <summary>
        /// 财务项目名称
        /// </summary>
        private string c_FIN_NAME;

        /// <summary>
        /// 财务项目父级代码
        /// </summary>
        private string c_FIN_CODE_P;

        /// <summary>
        /// 序号
        /// </summary>
        private int n_ORDER;

        /// <summary>
        /// 属性: C_FIN_CODE 
        /// </summary>
        [JsonProperty(PropertyName = "c_FIN_CODE")]
        public string C_FIN_CODE
        {
