            get { return c_FIN_CODE; }
        }

        /// <summary>
        /// 属性: C_FIN_NAME 
        /// </summary>
        [JsonProperty(PropertyName = "c_FIN_NAME")]
        public string C_FIN_NAME
        {
            set { c_FIN_NAME = value; }

            get { return c_FIN_NAME; }
        }

        /// <summary>
        /// 属性: C_FIN_CODE_P 
        /// </summary>
        [JsonProperty(PropertyName = "c_FIN_CODE_P")]
        public string C_FIN_CODE_P
        {
            set { c_FIN_CODE_P = value; }

            get { return c_FIN_CODE_P; }
        }

        /// <summary>
        /// 属性: N_ORDER 
        /// </summary>
        [JsonProperty(PropertyName = "n_ORDER")]
        public int N_ORDER
        {
            set { n_ORDER = value; }

            get { return n_ORDER; }
        }

    }
