package com.yss.ifa.szt.tool.license;

/**
 * 资源所属类型-二级分类
 * @author lwz
 *
 */
public enum ResourceTypeEnum {
	/**
	 * 伺服器主备服务
	 */
	SFQ_MAIN_STANDBY("主备服务"),
	
	/**
	 * 基础功能
	 */
	DZ_BASE("基础功能"),
	
	/**
	 * 电子对账对接机构
	 */
	DZ_ORG("机构"),
	
	DZ_TYPE("对账类型");
	
	/**
	 * 资源所属类型
	 */
	private String value = "";
	
	private ResourceTypeEnum(String value)
	{
		this.value = value;
	}
	
	/**
	 * 重写基类方法
	 */
	@Override
	public String toString() {
		return this.value.toString();
	}
}
