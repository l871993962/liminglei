package com.yss.ifa.szt.tool.log;

public enum ExeTypeEnum {

	/**
	 * 发送数据
	 */
	SEND("ER_SEND_LOG"),
	/**
	 * 接收数据
	 */
	RECEIVE("ER_RECEIVE_LOG");
	
	private String value = "";
	
	ExeTypeEnum(String value)
	{
		this.value = value;
	}
	
	@Override
	public String toString() {
		return this.value.toString();
	}

}
