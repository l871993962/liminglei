package com.yss.ams.base.information.modules.sys.convertdict.zhzd.dao;
/**
 * 转换字典表列名
 * @author zhao
 *
 */
public enum ZhzdColumnName {
	/**
	 * 组织架构代码
	 */
	c_GROUP_CODE("C_GROUP_CODE"),

	/**
	 * 源值
	 */
	c_S_CODE("C_S_CODE"),

	/**
	 * 转换值
	 */
	c_T_CODE("C_T_CODE"),
	
	id("C_IDEN"),

	operator("C_CHECK_BY"),

	auditDate("C_CHECK_TIME"),

	modifier("C_UPDATE_BY"),

	modifyDate("C_UPDATE_TIME"),

	auditState("N_CHECK_STATE");

	private String value;

	private ZhzdColumnName(String value) {
		this.value = value;
	}

	public String toString() {
		return this.value.toString();
	}

}
