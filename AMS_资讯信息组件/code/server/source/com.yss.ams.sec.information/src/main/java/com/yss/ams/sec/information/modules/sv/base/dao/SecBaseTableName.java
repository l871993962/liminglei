package com.yss.ams.sec.information.modules.sv.base.dao;

/**
 * 证券基本信息相关数据库表
 * @author 马向峰
 *
 */
public enum SecBaseTableName {
	userInfo("T_P_SV_SEC_BASE"),
	recycle("R_P_SV_SEC_BASE_R");
	
	private String value ;
	
	private SecBaseTableName(String value){
		this.value = value;
	}
	
	/* 重写toString方法：获取枚举
	 * @see java.lang.Enum#toString()
	 */
	public String toString(){
		return this.value.toString();
	}
}
