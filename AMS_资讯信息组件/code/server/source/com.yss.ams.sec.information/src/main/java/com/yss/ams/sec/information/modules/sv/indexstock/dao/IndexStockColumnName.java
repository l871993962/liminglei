public enum IndexStockColumnName {
	/**
	 * 指数代码
	 */
	c_INDEX_CODE("C_INDEX_CODE"),
	
	/**
	 * 成分券内码
	 */
	c_SEC_CODE("C_SEC_CODE"),

	/**
	 * 成分券上市代码
	 */
	c_SEC_MKT_CODE("C_SEC_MKT_CODE"),

	/**
	 * 启用日期
	 */
	d_BEGIN("D_BEGIN"),

	/**
	 * 描述信息
	 */
	c_DESC("C_DESC"),

	id("C_IDEN"),

	operator("C_CHECK_BY"),

	auditDate("C_CHECK_TIME"),

	modifier("C_UPDATE_BY"),

	modifyDate("C_UPDATE_TIME"),

	auditState("N_CHECK_STATE");

	private String value;

	private IndexStockColumnName(String value) {
		this.value = value;
	}

	public String toString() {
		return this.value.toString();
	}
}
