/**
 * @classDesc 债券付息元素
 * @version 1.0 2011-12-8
 * @author yh
 */
public class FiPayFactorBean {

	/**
	 * 证券代码
	 */
	private String secCode = null;
	
	/**
	 * 市场
	 */
	private String market = null;
	
	/**
	 * 发行面值
	 */
	private double issueFaceValue = 0;
	
	/**
	 * 票面利率
	 */
	private double couponRate = 0;
	
	/**
	 * 付息频率
	 */
	private String payFrequency = null;
	
	/**
	 * 债券起息日
	 */
	private Date bondBeginDate = null;
	
	/**
	 * 理财起息日STORY37137太平保险-理财品种信息增加付息日处理
	 */
	private Date licaiBeginDate = null;
	
	
	/**
	 * 债券截息日
	 */
	private Date bondEndDate = null;
	
	/**
	 * 计息日期类型
	 * // add by yh 2015.05.18 增加属性C_DV_ASSURE
	 */
	private String c_DV_ASSURE = null;

	/**
	 * @return the secCode
	 */
	public String getSecCode() {
		return secCode;
	}

	/**
	 * @param secCode the secCode to set
	 */
	public void setSecCode(String secCode) {
		this.secCode = secCode;
	}

	/**
	 * @return the issueFaceValue
	 */
	public double getIssueFaceValue() {
		return issueFaceValue;
	}

	/**
	 * @param issueFaceValue the issueFaceValue to set
	 */
	public void setIssueFaceValue(double issueFaceValue) {
		this.issueFaceValue = issueFaceValue;
	}

	/**
	 * @return the couponRate
	 */
	public double getCouponRate() {
		return couponRate;
	}

	/**
	 * @param couponRate the couponRate to set
	 */
	public void setCouponRate(double couponRate) {
		this.couponRate = couponRate;
	}

	/**
	 * @return the payFrequency
	 */
	public String getPayFrequency() {
		return payFrequency;
	}

	/**
	 * @param payFrequency the payFrequency to set
	 */
	public void setPayFrequency(String payFrequency) {
		this.payFrequency = payFrequency;
	}

	/**
	 * @return the beginDate
	 */
	public Date getBondBeginDate() {
		return bondBeginDate;
	}

	/**
	 * @param beginDate the beginDate to set
	 */
	public void setBondBeginDate(Date beginDate) {
		this.bondBeginDate = beginDate;
	}

	/**
	 * @return the endDate
	 */
	public Date getBondEndDate() {
		return bondEndDate;
	}

	/**
	 * @param endDate the endDate to set
	 */
	public void setBondEndDate(Date endDate) {
		this.bondEndDate = endDate;
	}

	/**
	 * @return the market
	 */
	public String getMarket() {
		return market;
	}

	/**
	 * @param market the market to set
	 */
	public void setMarket(String market) {
		this.market = market;
	}

	public String getC_DV_ASSURE() {
		return c_DV_ASSURE;
	}

	public void setC_DV_ASSURE(String c_DV_ASSURE) {
		this.c_DV_ASSURE = c_DV_ASSURE;
	}
	
	public Date getLicaiBeginDate() {
		return licaiBeginDate;
	}

	public void setLicaiBeginDate(Date licaiBeginDate) {
		this.licaiBeginDate = licaiBeginDate;
	}
	
	
	
}
