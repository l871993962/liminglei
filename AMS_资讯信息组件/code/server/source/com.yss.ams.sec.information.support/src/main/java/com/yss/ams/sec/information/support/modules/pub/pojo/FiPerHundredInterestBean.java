
package com.yss.ams.sec.information.support.modules.pub.pojo;

import java.util.Date;

/**
 * chenbo 2017-08-19 TASK #332232 
 * 公共信息处理界面中的系统初始化下的接口拆分到资讯组件中，以提供每百元利息生成功能
 * 
 */
/**
 * @classDesc 债券每百元利息信息bean
 * @version 1.0 2011-12-9
 * @author yh
 */
public class FiPerHundredInterestBean {
	
	/**
	 * 证券代码
	 */
	private String secCode = null;
	
	/**
	 * 计息日期
	 */
	private Date accrualDate = null;
	
	/**
	 * 报价方式
	 */
	private String quoteMode = null;
	
	/**
	 * 利息天数
	 */
	private int interestDays = 0;
	
	/**
	 * 票面利率
	 */
	private double couponRate = 0;
	
	/**
	 * 剩余本金
	 */
	private double remainMoney = 0;
	
	/**
	 * 税前每百元利息(买卖使用)
	 */
	private double beforeTaxInterest = 0;
	
	/**
	 * 税后每百元利息(买卖使用)
	 */
	private double afterTaxInterest = 0;
	
	/**
	 * 税前每百元利息(计息使用)
	 */
	private double beforeTaxInterestDue = 0;
	
	/**
	 * 税后每百元利息(计息使用)
	 */
	private double afterTaxInterestDue = 0;

	/**
	 * @return the secCode
	 */
	public String getSecCode() {
		return secCode;
	}

	/**
	 * @param secCode the secCode to set
	 */
	public void setSecCode(String secCode) {
		this.secCode = secCode;
	}

	/**
	 * @return the accrualDate
	 */
	public Date getAccrualDate() {
		return accrualDate;
	}

	/**
	 * @param accrualDate the accrualDate to set
	 */
	public void setAccrualDate(Date accrualDate) {
		this.accrualDate = accrualDate;
	}

	/**
	 * @return the quoteMode
	 */
	public String getQuoteMode() {
		return quoteMode;
	}

	/**
	 * @param quoteMode the quoteMode to set
	 */
	public void setQuoteMode(String quoteMode) {
		this.quoteMode = quoteMode;
	}

	/**
	 * @return the interestDays
	 */
	public int getInterestDays() {
		return interestDays;
	}

	/**
	 * @param interestDays the interestDays to set
	 */
	public void setInterestDays(int interestDays) {
		this.interestDays = interestDays;
	}

	/**
	 * @return the couponRate
	 */
	public double getCouponRate() {
		return couponRate;
	}

	/**
	 * @param couponRate the couponRate to set
	 */
	public void setCouponRate(double couponRate) {
		this.couponRate = couponRate;
	}

	/**
	 * @return the remainMoney
	 */
	public double getRemainMoney() {
		return remainMoney;
	}

	/**
	 * @param remainMoney the remainMoney to set
	 */
	public void setRemainMoney(double remainMoney) {
		this.remainMoney = remainMoney;
	}

	/**
	 * @return the beforeTaxInterest
	 */
	public double getBeforeTaxInterest() {
		return beforeTaxInterest;
	}

	/**
	 * @param beforeTaxInterest the beforeTaxInterest to set
	 */
	public void setBeforeTaxInterest(double beforeTaxInterest) {
		this.beforeTaxInterest = beforeTaxInterest;
	}

	/**
	 * @return the afterTaxInterest
	 */
	public double getAfterTaxInterest() {
		return afterTaxInterest;
	}

	/**
	 * @param afterTaxInterest the afterTaxInterest to set
	 */
	public void setAfterTaxInterest(double afterTaxInterest) {
		this.afterTaxInterest = afterTaxInterest;
	}

	/**
	 * @return the beforeTaxInterestDue
	 */
	public double getBeforeTaxInterestDue() {
		return beforeTaxInterestDue;
	}

	/**
	 * @param beforeTaxInterestDue the beforeTaxInterestDue to set
	 */
	public void setBeforeTaxInterestDue(double beforeTaxInterestDue) {
		this.beforeTaxInterestDue = beforeTaxInterestDue;
	}

	/**
	 * @return the afterTaxInterestDue
	 */
	public double getAfterTaxInterestDue() {
		return afterTaxInterestDue;
	}

	/**
	 * @param afterTaxInterestDue the afterTaxInterestDue to set
	 */
	public void setAfterTaxInterestDue(double afterTaxInterestDue) {
		this.afterTaxInterestDue = afterTaxInterestDue;
	}
	
	
	
}
